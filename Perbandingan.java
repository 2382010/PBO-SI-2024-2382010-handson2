public class Perbandingan {
    public static void main(String[] args) {
        int nilai = 10;
        int nilaiminimunKelulusan = 60;
        // == Sama dengan
        System.out.println(nilai == nilaiminimunKelulusan);
        // != Tidak sama dengan
        System.out.println(nilai != nilaiminimunKelulusan);
        // > lebih besar dari
        System.out.println(nilai > nilaiminimunKelulusan);
        // < lebih kecil dari
        System.out.println(nilai < nilaiminimunKelulusan);
        // >= lebih besar sama dengan
        System.out.println(nilai >= nilaiminimunKelulusan);
        // <= lebih kecil sama dengan
        System.out.println(nilai <= nilaiminimunKelulusan);
    }
}
